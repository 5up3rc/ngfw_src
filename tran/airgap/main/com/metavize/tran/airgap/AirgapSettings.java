package com.metavize.tran.airgap;

import java.io.Serializable;

import com.metavize.mvvm.security.Tid;

/**
 * Settings for the Airgap Transform.
 *
 * @author <a href="mailto:amread@metavize.com">Aaron Read</a>
 * @version 1.0
 * @hibernate.class
 * table="TR_AIRGAP_SETTINGS"
 */
public class AirgapSettings implements Serializable
{
    private static final long serialVersionUID = -565947423325544018L;

    private Long id;
    private Tid tid;

    public AirgapSettings() { }

    /**
     * @hibernate.id
     * column="ID"
     * generator-class="native"
     */
    private Long getId()
    {
        return id;
    }

    private void setId(Long id)
    {
        this.id = id;
    }

    /**
     * Transform id for these settings.
     *
     * @return tid for these settings
     * @hibernate.many-to-one
     * column="TID"
     * unique="true"
     * not-null="true"
     */
    public Tid getTid()
    {
        return tid;
    }

    public void setTid(Tid tid)
    {
        this.tid = tid;
    }
}
